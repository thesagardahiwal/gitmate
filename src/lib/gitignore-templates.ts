export const generateGitignore = (technologies: Technology[]): string => {
  if (technologies.length === 0) return "";
  
  const header = "# Generated by GitMate\\n";
  const content = technologies.map(tech => gitignoreTemplates[tech]).join("\\n");
  
  // Remove duplicate lines while preserving order (basic implementation)
  const lines = content.split("\\n");
  const uniqueLines = lines.filter((item, pos) => {
    if (item.trim() === "" || item.startsWith("#")) return true; // keep empty lines and comments
    return lines.indexOf(item) === pos;
  });
  
  return header + uniqueLines.join("\\n");
};
